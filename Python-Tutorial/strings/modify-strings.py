# Upper Case
a = "Hello, World!"
print(a.upper())

# Lower Case
a = "Hello, World!"
print(a.lower())

# Remove Whitespace
a = "Hello, World! "
print(a.strip()) # returns "Hellow, World!"

# Replace String
a = "Hello, World"
print(a.replace("H", "J"))

# Split String
a = "Hello, World!"
print(a.split(","))